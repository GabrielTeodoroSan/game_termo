    private void confirmBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmBtnActionPerformed
        String trytext = tryText.getText().toLowerCase();
        String estadoAtual = accepTxt.getText();
        
        if(trytext.length() > 1) {
            if(trytext.equals(palavra)){
                accepTxt.setText(palavra);
                tryText.setText("PARABÉNS !!!");
                
                tryText.setEnabled(false);
                confirmBtn.setEnabled(false);
                wordTxt.setEnabled(true);
                startBtn.setEnabled(true);
            }
            tryes++;
            tryesLabel.setText(String.valueOf(tryes));
        } else {
            StringBuilder novoEstado = new StringBuilder(estadoAtual);
            char letra = trytext.charAt(0);
            
            for (int i = 0; i < palavra.length(); i++) {
                if (palavra.charAt(i) == letra) {
                    novoEstado.setCharAt(i, palavra.charAt(i));
                }
            }
            
            if(trytext.equals(estadoAtual)) {
                accepTxt.setText(palavra);
                tryText.setText("PARABÉNS !!!");
                
                tryText.setEnabled(false);
                confirmBtn.setEnabled(false);
                wordTxt.setEnabled(true);
                startBtn.setEnabled(true);
            }
            
            accepTxt.setText(novoEstado.toString());
            tryText.setText("");
            tryes++;
            tryesLabel.setText(String.valueOf(tryes));
        }
    }//GEN-LAST:event_confirmBtnActionPerformed

