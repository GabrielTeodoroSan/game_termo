    private void startBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startBtnActionPerformed
        char[] senhaChars = wordTxt.getPassword();
        palavra = new String(senhaChars).replace(" ", "");
        
        StringBuilder tracos = new StringBuilder();
        for (int i = 0; i < palavra.length(); i++) {
            tracos.append("-");
        }
        accepTxt.setText(tracos.toString());
        
        wordTxt.setEnabled(false);
        startBtn.setEnabled(false);
        confirmBtn.setEnabled(true);
        tryText.setEnabled(true);
        tryText.setText("");
        tryesLabel.setText("0");
        tryes = 0;
    }//GEN-LAST:event_startBtnActionPerformed

