    private void carregarCategorias() {
        CategoriasDao dao = new CategoriasDao();
        List<Categoria> categorias = dao.collectCategoriasFromDatabase();

        jComboBox2.removeAllItems();

        for (Categoria cat : categorias) {
            jComboBox2.addItem(cat.getNome());
        }
    }
    
    private void carregarFornecedores() {
        FornecedoresDao dao = new FornecedoresDao();
        List<Fornecedores> fornecedores = dao.collectFornecedoresFromDatabase();
        
        jComboBox3.removeAllItems();
        
        for (Fornecedores f : fornecedores) {
            jComboBox3.addItem(f.getName());
        }
    }

