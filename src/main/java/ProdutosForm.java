    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String opcao = (String) jComboBox1.getSelectedItem();
        String nome = jTextField1.getText();
        
        ProdutosDao dao = new ProdutosDao();
        CategoriasDao cdao = new CategoriasDao();
        FornecedoresDao fdao = new FornecedoresDao();
        
        switch (opcao){
            case "Criar" -> {
                int quantidade = Integer.parseInt(jTextField2.getText());
                float preco = Float.parseFloat(jTextField3.getText());
                String categoria = (String) jComboBox2.getSelectedItem();
                String fornecedor = (String) jComboBox3.getSelectedItem();
                
                Produtos produto = new Produtos(nome, quantidade, preco, cdao.buscarCategoriaId(categoria), fdao.collectFornecedorId(fornecedor));
                
                dao.createProduto(produto);
                
            }
            case "Buscar" -> {
                DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
                model.setRowCount(0);
                model.setColumnIdentifiers(new String[]{"Nome", "Quantidade", "Preço", "Categoria"});

                if (nome.isEmpty()){
                    try {
                        List<Produtos> produtos = dao.getProdutos();
                        for (Produtos p : produtos) {
                            System.out.println("Opa, " + p.getNome());
                            model.addRow(new Object[]{
                                p.getNome(),
                                p.getQuantidade(),
                                p.getPreco(),
                                p.getCategoria()
                            });
                        }
                    } catch (SQLException e) {
                        JOptionPane.showMessageDialog(null, "Erro ao listar produtos: " + e.getMessage());
                    }
                } else {
                    try {
                        Produtos produto = dao.getProduto(nome);
                        model.addRow(new Object[]{produto.getNome(), produto.getQuantidade(), produto.getPreco(), produto.getCategoria()});
                    } catch (SQLException e){
                        JOptionPane.showMessageDialog(null, "Erro ao listar produtos: " + e.getMessage());
                    }
                }
            }
            case "Deletar" -> {
                dao.deleteProdutoPorNome(nome);
            }
            case "Atualizar" -> {
                int quantidade = Integer.parseInt(jTextField2.getText());
                float preco = Float.parseFloat(jTextField3.getText());
                String categoria = (String) jComboBox2.getSelectedItem();
                String fornecedor = (String) jComboBox3.getSelectedItem();
                
                Produtos produto = new Produtos(nome, quantidade, preco, cdao.buscarCategoriaId(categoria), fdao.collectFornecedorId(fornecedor));
                dao.updateProdutoPorNome(nome, produto);
            }
            default -> {
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

